function Student(
  fullname,
  masv,
  gender,
  birthday,
  cityzen,
  school,
  email,
  phone,
  hometown,
  room
) {
  this.fullname = fullname;
  this.masv = masv;
  this.gender = gender;
  this.birthday = birthday;
  this.cityzen = cityzen;
  this.school = school;
  this.email = email;
  this.phone = phone;
  this.hometown = hometown;
  this.room = room;
}
